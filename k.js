// round, ceil & floor

console.log(Math.round(4.7)); //pembulatan ke yg lebih dekat
console.log(Math.round(4.4)); 
console.log(Math.floor(4.7)); //dibulatkan ke bawah
console.log(Math.ceil(4.4)); // dibulatkan ke atas

//random, max & min
console.log(Math.random());
console.log(Math.max(1,3,5));
console.log(Math.min(1,3,5));